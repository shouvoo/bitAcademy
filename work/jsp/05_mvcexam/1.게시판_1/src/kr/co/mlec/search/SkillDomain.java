package kr.co.mlec.search;

public class SkillDomain
{
	int skillCode;
	String SkillName;
	public int getSkillCode() {
		return skillCode;
	}
	public void setSkillCode(int skillNumber) {
		this.skillCode = skillNumber;
	}
	public String getSkillName() {
		return SkillName;
	}
	public void setSkillName(String skillName) {
		SkillName = skillName;
	}
	
	
}
