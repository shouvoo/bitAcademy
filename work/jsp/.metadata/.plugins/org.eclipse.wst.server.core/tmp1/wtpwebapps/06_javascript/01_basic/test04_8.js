/*
    자바스크립트 변수 - 9
    자바스크립트의 변수는 동적 타입이기 때문에 하나의 변수에 다양한 값을 대입할수 있다
*/

"use strict"

var val = 10;
console.log(val, typeof(val));

val = 10.3;
console.log(val, typeof(val));

val = "test";
console.log(val, typeof(val));


val = true;
console.log(val, typeof(val));

val = {};
console.log(val, typeof(val));

val = undefined;
console.log(val, typeof(val));

val = null;
console.log(val, typeof(val));






